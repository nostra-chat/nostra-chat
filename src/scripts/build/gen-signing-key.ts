#!/usr/bin/env tsx
/**
 * One-off key generation for release signing.
 * DO NOT COMMIT signing-private.key. Copy into GitHub Actions secret
 * UPDATE_SIGNING_KEY (base64), then delete from disk.
 */
import {writeFileSync, existsSync} from 'fs';
import {join} from 'path';
import * as ed from '@noble/ed25519';
import {sha512} from '@noble/hashes/sha512';

// Bridge sync hash impl for @noble/ed25519 v2 sync paths (optional; fine for keygen)
ed.etc.sha512Sync = (...m: Uint8Array[]) => sha512(ed.etc.concatBytes(...m));

function base64(buf: Uint8Array): string {
  return Buffer.from(buf).toString('base64');
}

async function main() {
  const priv = ed.utils.randomPrivateKey();
  const pub = await ed.getPublicKeyAsync(priv);
  const outDir = process.cwd();
  const privPath = join(outDir, 'signing-private.key');
  const pubPath = join(outDir, 'src/lib/update/signing/trusted-pubkey.generated.ts');

  if(existsSync(privPath)) {
    console.error(`${privPath} already exists — aborting to avoid overwriting a key.`);
    process.exit(1);
  }

  writeFileSync(privPath, base64(priv) + '\n', {mode: 0o600});

  const fingerprint = 'ed25519:' + Buffer.from(pub).toString('hex').slice(0, 16);
  const pubTs = `/* eslint-disable */\n// GENERATED by src/scripts/build/gen-signing-key.ts\n// Commit this file. Do NOT commit signing-private.key.\nexport const TRUSTED_PUBKEY_B64 = '${base64(pub)}';\nexport const TRUSTED_PUBKEY_FINGERPRINT = '${fingerprint}';\n`;
  writeFileSync(pubPath, pubTs);

  console.log('Private key written to:', privPath);
  console.log('Public key baked into:', pubPath);
  console.log('Fingerprint:', fingerprint);
  console.log('\nNext steps:');
  console.log('1. Copy the content of signing-private.key into GitHub Actions secret UPDATE_SIGNING_KEY');
  console.log('2. Delete signing-private.key from disk: rm signing-private.key');
  console.log('3. Commit the generated trusted-pubkey.generated.ts');
}
main().catch((e) => { console.error(e); process.exit(1); });
